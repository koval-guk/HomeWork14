package coffee.order;

public class Order {
    private int id;
    private String name;

    public Order() {
        name = "NoName";
    }

    public Order(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Order{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
